import * as React from "react"
import { cn } from "./../../lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { highlightLeft?: boolean }
>(({ className, highlightLeft = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "relative overflow-hidden rounded-xl bg-[#FFFFFF] text-[#2E2E2E] shadow-sm transition-shadow hover:shadow-md border border-[#F3E5DC]",
      highlightLeft && "border-l-4 border-l-[#F97362]",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

